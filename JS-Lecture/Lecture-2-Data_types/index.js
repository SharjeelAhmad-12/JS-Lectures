// Var Data Type 

var firstName = "Sharjeel"; // Declare 'name' and assign a value
console.log(firstName); // Output: Sharjeel

firstName = "Azlan"; // Reassign a new value
console.log(firstName); // Output: Azlan

var firstName = "Haider"; // Redeclare the same variable
console.log(firstName); // Output: Haider

// ------------------------------------------------------------------------------------------------

// Let Data Type

let age = 25; // Declare 'age' and assign a value
console.log(age); // Output: 25

age = 30; // Reassign a new value
console.log(age); // Output: 30

